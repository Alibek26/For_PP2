#Creating a Function!!!
#In Python, a function is defined using the "def" keyword, followed by a function name and parentheses:
#Example 
def my_function():
  print("Hello from a function")
  
#Calling a Function!!!
#To call a function, write its name followed by parentheses:
#Example 1
def my_function():
  print("Hello from a function")

my_function()

#Example 2
def my_function():
  print("Hello from a function")

my_function()
